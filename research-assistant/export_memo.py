"""Generate a self-contained HTML research memo from pipeline output."""

from datetime import date


def generate_html_memo(question: str, result: dict, case_links: list, countries: list[str]) -> str:
    today = date.today().strftime("%B %d, %Y")

    country_sections = ""
    for country in countries:
        summary = result["country_summaries"].get(country, "")
        country_sections += f"""
        <div class="jurisdiction">
          <h3>{country}</h3>
          <p>{summary.replace(chr(10), "<br>")}</p>
        </div>"""

    theory_section = result.get("theory_note", "")

    links_html = ""
    if case_links:
        items = ""
        for c in case_links:
            title = c.get("title", "Untitled")[:100]
            meta = f"{c.get('country','')} · {c.get('year','')} · {c.get('category','')}"
            win = c.get("win_loss", "not_coded")
            url = c.get("url", "")
            if url:
                items += f'<li><a href="{url}" target="_blank">{title}</a><br><small>{meta} — {win}</small></li>'
            else:
                items += f"<li>{title}<br><small>{meta} — {win}</small></li>"
        links_html = f"<h2>Key Cases</h2><ul>{items}</ul>"

    answer_html = result["final_answer"].replace("\n\n", "</p><p>").replace("\n", "<br>")

    return f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>Legal Last Mile Research Memo</title>
<style>
  body {{ font-family: Georgia, serif; max-width: 800px; margin: 40px auto; padding: 0 20px;
          color: #1a1a1a; line-height: 1.7; }}
  .header {{ border-bottom: 2px solid #1e3a5f; padding-bottom: 16px; margin-bottom: 28px; }}
  .header h1 {{ font-size: 1.4em; color: #1e3a5f; margin: 0 0 8px; }}
  .meta {{ font-size: 0.85em; color: #555; }}
  .question {{ background: #f0f4ff; border-left: 4px solid #2563EB;
               padding: 12px 16px; margin: 20px 0; font-style: italic; }}
  h2 {{ color: #1e3a5f; border-bottom: 1px solid #ddd; padding-bottom: 6px;
        margin-top: 32px; font-size: 1.1em; text-transform: uppercase;
        letter-spacing: 0.05em; }}
  h3 {{ color: #374151; font-size: 1em; margin: 20px 0 6px; }}
  .jurisdiction {{ background: #f9fafb; border: 1px solid #e5e7eb;
                   border-radius: 4px; padding: 14px 16px; margin: 12px 0; }}
  a {{ color: #2563EB; }}
  ul {{ padding-left: 20px; }} li {{ margin: 8px 0; }}
  .footer {{ margin-top: 40px; padding-top: 16px; border-top: 1px solid #ddd;
             font-size: 0.8em; color: #777; }}
</style>
</head>
<body>
<div class="header">
  <h1>Legal Last Mile Research Memo</h1>
  <div class="meta">
    Generated: {today} &nbsp;·&nbsp;
    Jurisdictions: {', '.join(countries)} &nbsp;·&nbsp;
    Dataset: 83,596 cases (1997–2026)
  </div>
</div>

<div class="question">{question}</div>

<h2>Research Finding</h2>
<p>{answer_html}</p>

<h2>Jurisdiction Analyses</h2>
{country_sections}

<h2>Legal Last Mile Theory Application</h2>
<p>{theory_section.replace(chr(10), "<br>")}</p>

{links_html}

<div class="footer">
  <strong>AI Disclosure</strong> — Generated by the Legal Last Mile Research Assistant using
  Claude (Anthropic) and the Global Water Law Dataset
  (<a href="https://doi.org/10.5281/zenodo.19836413">DOI 10.5281/zenodo.19836413</a>).
  This memo reflects dataset patterns and does not constitute legal advice.
  Tool: <a href="https://github.com/jrklaus8/water-law-dataset/tree/main/research-assistant">
  github.com/jrklaus8/water-law-dataset</a>
</div>
</body>
</html>"""
