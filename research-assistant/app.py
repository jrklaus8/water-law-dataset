import os
from pathlib import Path
import streamlit as st
from dotenv import load_dotenv
import anthropic

load_dotenv(Path(__file__).parent / ".env")

from data_loader import (
    load_dataset,
    compute_stats,
    detect_country,
    build_country_contexts,
    build_context,
    GOVERNANCE_LABELS,
)
from agents import run_pipeline

ANTHROPIC_API_KEY = os.getenv("ANTHROPIC_API_KEY", "")


def render_sidebar(stats: dict):
    st.sidebar.title("Dataset Overview")
    st.sidebar.markdown("**83,596 court decisions · 3 countries · 1997–2026**")
    st.sidebar.markdown("---")
    for country, s in stats.items():
        with st.sidebar.expander(f"{country} ({s['total']:,} cases)"):
            top = sorted(s["governance_counts"].items(), key=lambda x: -x[1])[:6]
            for cat, n in top:
                label = GOVERNANCE_LABELS.get(cat, cat)
                pct = n / s["total"] * 100
                st.write(f"**{label}**: {n:,} ({pct:.1f}%)")
            st.write(f"User wins: {s['user_wins']} | Utility wins: {s['utility_wins']}")
            st.write(f"HR-language cases: {s['hr_language']}")
    st.sidebar.markdown("---")
    st.sidebar.markdown("**Pipeline**")
    st.sidebar.caption(
        "Each query runs through a multi-agent pipeline:\n"
        "1. Country specialist agents (one per jurisdiction)\n"
        "2. Legal Last Mile theory agent\n"
        "3. Synthesis agent (final answer)"
    )
    st.sidebar.markdown("---")
    st.sidebar.caption(
        "**AI Disclosure** — Responses generated by Claude (Anthropic) "
        "analysing the Global Water Law Dataset (jrklaus8/water-law-dataset). "
        "Not legal advice."
    )


def run_research(question: str, df, stats: dict) -> dict:
    client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)
    country_contexts = build_country_contexts(df, question, stats)
    countries = list(country_contexts.keys())

    status_placeholder = st.empty()
    log = []

    def progress(msg: str):
        log.append(msg)
        status_placeholder.info(" → ".join(log))

    result = run_pipeline(client, question, country_contexts, progress_callback=progress)
    status_placeholder.empty()
    return result, countries, country_contexts


def render_pipeline_result(result: dict, countries: list[str], country_contexts: dict):
    st.markdown(result["final_answer"])

    with st.expander("Agent pipeline trace"):
        st.markdown(
            f"**Jurisdictions analysed:** {', '.join(countries)}  \n"
            f"**Agents run:** {len(countries)} country specialist(s) + 1 theory agent + 1 synthesis agent"
        )
        for country in countries:
            st.markdown(f"#### {country} specialist")
            st.markdown(result["country_summaries"][country])
        st.markdown("#### Legal Last Mile theory agent")
        st.markdown(result["theory_note"])
        st.markdown("---")
        st.markdown("**Raw data fed to country agents:**")
        for country, ctx in country_contexts.items():
            with st.expander(f"{country} data context"):
                st.text(ctx[:1500] + ("…" if len(ctx) > 1500 else ""))


def main():
    st.set_page_config(
        page_title="Legal Last Mile Research Assistant",
        page_icon="💧",
        layout="wide",
    )

    if not ANTHROPIC_API_KEY:
        st.error("ANTHROPIC_API_KEY not set. Add it to a .env file or set it as an environment variable.")
        st.stop()

    with st.spinner("Loading dataset…"):
        df = load_dataset()
        stats = compute_stats(df)

    render_sidebar(stats)

    st.title("💧 Legal Last Mile Research Assistant")
    st.markdown(
        "Ask research questions about water law across **Brazil**, **Netherlands**, and **Canada** "
        "— grounded in 83,596 court decisions, analysed by a multi-agent pipeline."
    )

    st.info(
        "**AI Disclosure** — This tool uses Claude (Anthropic) to analyse a curated legal dataset. "
        "Responses reflect dataset patterns, not comprehensive legal research. Not legal advice.",
        icon="ℹ️",
    )

    st.markdown("---")

    st.markdown("**Try a question:**")
    examples = [
        "Why does Brazil have so many tariff disputes compared to the Netherlands?",
        "What happens to residents of informal settlements who seek water connections in Brazil?",
        "How do Dutch courts handle connection refusal cases differently from Brazilian courts?",
        "Does using human rights language in water disputes improve outcomes for users?",
        "What does the data reveal about the Legal Last Mile theory across all three countries?",
    ]
    cols = st.columns(len(examples))
    selected_example = None
    for col, ex in zip(cols, examples):
        if col.button(ex, use_container_width=True):
            selected_example = ex

    st.markdown("---")

    if "messages" not in st.session_state:
        st.session_state.messages = []

    for msg in st.session_state.messages:
        with st.chat_message(msg["role"]):
            st.markdown(msg["content"])
            if "pipeline" in msg:
                p = msg["pipeline"]
                render_pipeline_result(p["result"], p["countries"], p["country_contexts"])

    question = selected_example or st.chat_input("Ask a research question about water law…")

    if question:
        st.session_state.messages.append({"role": "user", "content": question})
        with st.chat_message("user"):
            st.markdown(question)

        with st.chat_message("assistant"):
            result, countries, country_contexts = run_research(question, df, stats)
            render_pipeline_result(result, countries, country_contexts)

        st.session_state.messages.append({
            "role": "assistant",
            "content": result["final_answer"],
            "pipeline": {
                "result": result,
                "countries": countries,
                "country_contexts": country_contexts,
            },
        })

    if st.session_state.messages:
        if st.button("Clear conversation"):
            st.session_state.messages = []
            st.rerun()


if __name__ == "__main__":
    main()
